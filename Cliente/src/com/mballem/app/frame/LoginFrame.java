    private void BtEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtEntrarActionPerformed
        // TODO add your handling code here:
        String user = txtLogin.getText();
        String pass = txtSenha.getText();
        
        try{
          if(user.equals("adm") || user.equals("ADM")){
             this.consulta = new ConsultaFrame();
             consulta.setVisible(true); 
             this.dispose();            
          }else{
             this.banco = new Metodos_Banco(0, null, null, pass, null, null);             
             this.banco.setNome(user);
             this.banco.login();
             this.dispose();
        }
        }catch(Exception e){
           JOptionPane.showMessageDialog(null,"Us√°rio ou senha incorretos!\n" + e.toString(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtEntrarActionPerformed

