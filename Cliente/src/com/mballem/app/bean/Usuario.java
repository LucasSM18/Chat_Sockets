package com.mballem.app.bean;

/**
 *
 * @author User
 */
public class Usuario extends ChatMessage{

    //variaveis para coletar informações para o cadastro do cliente
    private int idade;
    private String sexo;
    private String email;
    private String senha;
    private String telefone;
    private String celular;
    
    public Usuario(int idade, String sexo, String email, String senha, String telefone, String celular){
       this.idade = idade;
       this.sexo = sexo;
       this.email = email;
       this.senha = senha;
       this.telefone = telefone;
       this.celular = celular;
    }
       
    /**
     * @return the idade
     */
    public int getIdade() {
        return idade;
    }

    /**
     * @param idade the idade to set
     */
    public void setIdade(int idade) {
        this.idade = idade;
    }

    /**
     * @return the sexo
     */
    public String getSexo() {
        return sexo;
    }

    /**
     * @param sexo the sexo to set
     */
    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the senha
     */
    public String getSenha() {
        return senha;
    }

    /**
     * @param senha the senha to set
     */
    public void setSenha(String senha) {
        this.senha = senha;
    }

    /**
     * @return the telefone
     */
    public String getTelefone() {
        return telefone;
    }

    /**
     * @param telefone the telefone to set
     */
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    /**
     * @return the celular
     */
    public String getCelular() {
        return celular;
    }

    /**
     * @param celular the celular to set
     */
    public void setCelular(String celular) {
        this.celular = celular;
    }
       
}
